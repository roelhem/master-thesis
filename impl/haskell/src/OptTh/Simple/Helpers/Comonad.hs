----------------------------------------------------------------------------
------------------------------ ENV HELPERS ---------------------------------
----------------------------------------------------------------------------

ask' :: (ComonadEnv e w, o ~> Getter) => o e t a b -> w x -> a
ask' o = asks (get o)

local' :: (o ~> Setter) => o e e' a a' -> (a -> a') -> EnvT e w x -> EnvT e' w x
local' o f = local (over o f)

